// Search helper
export function searchVehicles(query: string, typeFilter?: VehicleType | "all"): Vehicle[] {
  const q = query.toLowerCase().trim();
  if (!q && (!typeFilter || typeFilter === "all")) return VEHICLES;

  return VEHICLES.filter((v) => {
    const matchesType = !typeFilter || typeFilter === "all" || v.type === typeFilter;
    if (!matchesType) return false;
    if (!q) return true;

    const haystack = [
      v.name,
      v.brand,
      v.slug,
      v.tagline,
      v.type,
      v.body,
      ...v.searchTags,
    ].join(" ").toLowerCase();

    return q.split(" ").every((word) => haystack.includes(word));
  });
}

